var pageSize = 5;
var pageIndex = 1;
function setItem(arr, pageIndex){
    debugger;
    $("#list-items").html('');
    var start = (pageIndex > 1) ? (pageSize * (pageIndex - 2) + pageSize) : 0;
    var end =   pageSize * (pageIndex - 1) + pageSize;
    for(var  i = start; i < end && i < arr.length; i++){
        renderItem(arr[i]);
    }
}
function renderItem(obj){
    var item = `<div class="col-3 mb-4">
                    <div class="card">
                        <img class="card-img-top" src="${obj.image}" alt="Card image cap">
                        <div class="card-body">
                        <h5 class="card-title">${obj.name}</h5>
                        <p class="card-text">${obj.content}</p>
                        <a href="#" class="btn btn-primary">Go somewhere</a>
                        </div>
                    </div>
                </div>`;
    $("#list-items").append(item);
}

